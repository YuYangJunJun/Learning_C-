#include <iostream>
using namespace std;
int main24()
{


    //do_while语句
    //在屏幕中输出0~9这10个数字

    int num = 0;
    do
    {

        cout << num << endl;
        num++;
    } while (num < 10);
    //do...while和while循环的区别在于do...while先执行后判段



    system("pause");
    return 0;
}